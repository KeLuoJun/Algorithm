#include <stdio.h>
int main()
{
    int i,j,n=0;
    int num[n];
    int k;
    scanf("%d",&k);
    
    for(i=100; i<=999; i++){
        for(j=1; j<=i; j++){
            if(i==j*j){
                num[n]=i;
                n++;
                
        }
    }
    }
    printf("%d",num[k-1]);
    return 0;
    
}
